// Build-time: generate an equirectangular world land path (viewBox 0 0 1000 500) so the runtime
// component ships a single static SVG path instead of d3 + a topojson file. Run: node scripts/gen-worldmap.mjs
import fs from 'fs';
import { geoEquirectangular, geoPath } from 'd3-geo';
import { feature } from 'topojson-client';

const topo = JSON.parse(fs.readFileSync('node_modules/world-atlas/land-110m.json', 'utf8'));
const land = feature(topo, topo.objects.land);

// scale 159.154943 + translate [500,250] maps the whole globe exactly onto 1000x500, so a pin at
// [lng,lat] is simply x=(lng+180)*1000/360, y=(90-lat)*500/180 — no projection lib needed at runtime.
const projection = geoEquirectangular().scale(1000 / (2 * Math.PI)).translate([500, 250]);
const d = geoPath(projection)(land);

const out = `// AUTO-GENERATED by scripts/gen-worldmap.mjs — equirectangular world land, viewBox "0 0 1000 500".
// Pin projection that matches this path: x=(lng+180)*1000/360, y=(90-lat)*500/180.
export const LAND_PATH =\n  ${JSON.stringify(d)};\n`;
fs.writeFileSync('src/lib/worldwide/worldmap-data.ts', out);
console.log('wrote src/lib/worldwide/worldmap-data.ts —', d.length, 'chars');
